from types import SimpleNamespace

MAX_X = 50
MAX_Y = 50


class Knot:
    def __init__(self):
        self.init_state = (MAX_Y // 2, MAX_X // 2)
        init_x, init_y = self.init_state
        self.H = SimpleNamespace(x=init_x, y=init_y)
        self.T = SimpleNamespace(x=init_x, y=init_y)
        self.positions = [self.init_state]

    def adjacent(self):
        if self.H.x == self.T.x:
            return abs(self.H.y - self.T.y) <= 1
        if self.H.y == self.T.y:
            return abs(self.H.x - self.T.x) <= 1
        return abs(self.H.y - self.T.y) == 1 and abs(self.H.x - self.T.x) == 1

    def same_row(self):
        return self.H.y == self.T.y

    def same_column(self):
        return self.H.x == self.T.x

    def move_horizontal(self, n, direction=1):
        for _ in range(int(n)):
            self.H.x += 1 * direction
            while not self.adjacent():
                if not self.same_row():
                    self.T.y = self.H.y
                self.T.x += 1 * direction
                self.positions.append((self.T.x, self.T.y))

    def move_vertical(self, n, direction=1):
        for _ in range(int(n)):
            self.H.y += 1 * direction
            while not self.adjacent():
                if not self.same_column():
                    self.T.x = self.H.x
                self.T.y += 1 * direction
                self.positions.append((self.T.x, self.T.y))


def show_table(knot):
    table = [["." for _ in range(MAX_X)] for _ in range(MAX_Y)]
    for x, y in knot.positions:
        table[y][x] = "#"
    x, y = knot.init_state
    table[x][y] = "s"
    table[knot.H.y][knot.H.x] = "H"
    table[knot.T.y][knot.T.x] = "T"
    if knot.same_row() and knot.same_column():
        table[knot.H.y][knot.H.x] = "O"

    for row in table:
        print("".join(row))


